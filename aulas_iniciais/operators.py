# + -> some
# - -> subtration
# * -> multiplication
# / -> division
# // -> integer result
# ** -> pontentiation
# % -> rest result
# ( ) -> precedences

 
print('Multiplication * ->', 2 * 5)
print('Some + ->', 2 + 5)
print('Subtration - ->', 10 - 5)
print('Division / ->', 12 / 5)
print('Integer Result Division // ->', 5 // 2)
print('Rest Result Division % ->', 12 % 5)
print('Potentiation ** ->', 2 ** 3)


